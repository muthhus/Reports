class Widget < ActiveRecord::Base

  # Constants ======================================================================================

  # Named scopes ===================================================================================

  # Relationships ==================================================================================
  has_many :widget_stats
  belongs_to :locations
  
  # Behaviours =====================================================================================

  # Callbacks ======================================================================================

  # Validations ====================================================================================

  # Class methods ==================================================================================

  # Instance methods ===============================================================================

  def date
    self.stat.date
  end
  
  def name
    self[:name].titleize
  end
  
  def stat
    self.widget_stats.last
  end
  
  def quantity
    self.stat.quantity
  end
  
  # Initialization =================================================================================

end
